public class PictureBean {

    /**
     * pictureName : mouse01
     * hScale : 0.708
     * wScale : 0.708
     * yScale : 0.2325
     * xScale : 0.1495
     */

    private String pictureName;
    private String hScale;
    private String wScale;
    private String yScale;
    private String xScale;

    public String getPictureName() {
        return pictureName;
    }

    public void setPictureName(String pictureName) {
        this.pictureName = pictureName;
    }

    public String getHScale() {
        return hScale;
    }

    public void setHScale(String hScale) {
        this.hScale = hScale;
    }

    public String getWScale() {
        return wScale;
    }

    public void setWScale(String wScale) {
        this.wScale = wScale;
    }

    public String getYScale() {
        return yScale;
    }

    public void setYScale(String yScale) {
        this.yScale = yScale;
    }

    public String getXScale() {
        return xScale;
    }

    public void setXScale(String xScale) {
        this.xScale = xScale;
    }
}
